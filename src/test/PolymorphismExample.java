package test;

public class PolymorphismExample {
	public void add(int a,int b) {
		System.out.println("Sum is: "+(a+b)) ;
	}
	
	public void add(int a,int b, int c) {
		System.out.println("Sum is: "+(a+b+c)) ;
	}
	
	public void add(double a,double b) {
		System.out.println("Sum is: "+(a+b)) ;
	}

}
